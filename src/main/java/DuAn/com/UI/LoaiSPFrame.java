    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        try {
            //kiểm tra mã không trùng, kiểm tra rổnng
            if (kiemTraRong() && kiemTraTrungMa() == false && !kiemTraTrungTen()) {
                ketNoiCsdl();
                String sql = "INSERT INTO LOAI_SP(ID_LSP,TEN_LOAI_SP,GHICHU) VALUES(?, ?, ?)";
                PreparedStatement cauLenh = ketNoi.prepareStatement(sql);
                cauLenh.setString(1, txtIDLSP.getText());
                cauLenh.setString(2, txtLoaiSP.getText());
                cauLenh.setString(3, txtGhiChu.getText());

                cauLenh.executeUpdate();// có thay đổi thì dùng excuteUpdate (thêm sửa xoá)
                TaiDulieuVaoBang();
                JOptionPane.showMessageDialog(this, "Thêm thành công!");
            } else {
                JOptionPane.showMessageDialog(this, "Thêm thất bại hãy kiểm tra lại!");
            }

            ketNoi.close();
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(LoaiSPFrame.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(LoaiSPFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnThemActionPerformed

