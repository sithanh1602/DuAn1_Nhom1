    public void TaiDulieuVaoBang() throws ClassNotFoundException, SQLException {
        model.setRowCount(0);
        ketNoiCsdl();
        String sql = "SELECT * FROM LOAI_SP";
        PreparedStatement cauLenh = ketNoi.prepareStatement(sql);
        ResultSet ketQua = cauLenh.executeQuery();
        while (ketQua.next() == true) {
            String idLSP = ketQua.getString(1);
            String TenLSP = ketQua.getString(2);
            String GhiChu = ketQua.getString(3);
            model.addRow(new Object[]{idLSP, TenLSP, GhiChu});
        }
        tblLSP.setModel(model);
        ketNoi.close();
    }

