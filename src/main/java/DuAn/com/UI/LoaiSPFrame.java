    //bắt lỗi form
    public boolean kiemTraRong() {
        if (txtIDLSP.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Chưa nhập mã loại sản phẩm!");
            return false;
        }
        if (txtLoaiSP.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Chưa nhập tên loại sản phẩm!");
            return false;
        }
        return true;
    }


    public boolean kiemTraTrungTen() throws ClassNotFoundException, SQLException {
        ketNoiCsdl();
        String sql = "SELECT TEN_LOAI_SP FROM LOAI_SP WHERE TEN_LOAI_SP = ?";
        PreparedStatement cauLenh = ketNoi.prepareStatement(sql);
        cauLenh.setString(1, txtLoaiSP.getText());
        ResultSet ketQua = cauLenh.executeQuery();
        if (ketQua.next()) {
            JOptionPane.showMessageDialog(this, "Tên loại sản phẩm đã tồn tại!");
            return true;
        }
        return false;
    }

    public boolean kiemTraTrungMa() throws ClassNotFoundException, SQLException {
        ketNoiCsdl();
        String sql = "SELECT ID_LSP FROM LOAI_SP";// có thể bắt cccd hay sdt ...
        PreparedStatement cauLenh = ketNoi.prepareStatement(sql);
        ResultSet ketQua = cauLenh.executeQuery();
        while (ketQua.next() == true) {
            if (ketQua.getString(1).equalsIgnoreCase(txtIDLSP.getText())) { // dò mã nhập vào kết quả coi có trùng ko có bắt lỗi
                JOptionPane.showMessageDialog(this, "Mã số hoá đơn đã tồn tại!");
                return true;
            }
        }
        return false;// mã sinh viên không trùng 
    }

